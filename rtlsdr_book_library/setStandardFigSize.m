function setStandardFigSize(figNum)
% set the Figure to a pre-set size and position, with respect
% to the current screen resolution

% find the screen resolution
% (note this assumes the resolution has not been changed
% since MATLAB was opened)
screensize = get(0,'ScreenSize');
scr_left = screensize(1);
scr_bottom = screensize(2);
scr_width = screensize(3);
scr_height = screensize(4);

% set figure size as proportion of screen width and height
fig_height = 0.5*scr_height;
fig_width = 0.5*scr_width;

% set figure position w.r.t. bottom right (origin is bottom left)
fig_pos_vert = scr_bottom + (0.05*scr_height);
fig_pos_horz = scr_left + (0.95*scr_width) - fig_width;

set(figNum,'position', [fig_pos_horz, fig_pos_vert, fig_width, fig_height]);

